package LoopingStatment;

public class divide7 {
    public static void main(String[] args) {
        int i = 70;
        do{
            if (i%7 == 0){
                System.out.print(i+" ");
            }
            i++;
        }while (i<=90);

        System.out.println();
        System.out.println("------------------");
    }
}
